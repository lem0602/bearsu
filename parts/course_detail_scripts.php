    $('.fa-bookmark').click(function() {
        if ($(this).hasClass('fa-regular')) {
            $(this).addClass('fa-solid')
            $(this).removeClass('fa-regular')
        } else {
            $(this).removeClass('fa-solid')
            $(this).addClass('fa-regular')
        }
    })
